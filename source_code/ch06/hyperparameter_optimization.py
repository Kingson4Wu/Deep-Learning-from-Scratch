# 使用 MNIST 数据集进行超参数的最优化。这里我们将学习 率和控制权值衰减强度的系数(下文称为“权值衰减系数”)这两个超参数的 搜索问题作为对象。这个问题的设定和解决思路参考了斯坦福大学的课程“CS231n”。
# 以权值衰减系数为 10−8 到 10−4、学习率为 10−6 到 10−2 的范围 进行实验
# 实线是验证数据的识别精度，虚线是训练数据的识别精度